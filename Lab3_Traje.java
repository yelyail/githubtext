package FinalLabActivity;

public class Lab3_Traje {
	public static void main(String [] args) {
		int x = 5;
		int y = 8;
		
		System.out.print("The sum of x and y is ");
		System.out.println( x + y);
		
		System.out.print("The difference of x and y is ");
		System.out.println( x - y);
		
		System.out.print("The product of x and y is ");
		System.out.println( x * y); 
		
		System.out.print("The quotient of x and y is ");
		System.out.println( x / y);
		
		System.out.print("The remainder of x and y is ");
		System.out.println( x % y);
		
	}
}
